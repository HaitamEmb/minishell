#include "minishell.h"

int	ft_isalnum(int c)
{
	return ((c >= '0' && c <= '9') || (c >= 'a' && c <= 'z')
		|| (c >= 'A' && c <= 'Z'));
}

int	ft_isnum(int c)
{
	return (c >= '0' && c <= '9');
}

int	is_numeric_str(const char *str)
{
	int	i;

	i = 0;
	if (!str || !str[0])
		return (0);
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i])
	{
		if (!ft_isnum(str[i]))
			return (0);
		i++;
	}
	return (1);
}
